#!/usr/bin/env python3
"""
Collect bugs generated by lm_unified_bugs config and prepare for validation.
Creates a JSON file with bug patches that can be validated with F2P/P2P tests.
"""

import json
import os
from pathlib import Path
from datetime import datetime

BUG_GEN_DIR = Path("logs/bug_gen/vllm-project__vllm.3e1ad443")
OUTPUT_FILE = "vllm_lm_unified_bugs_for_validation.json"

def collect_bugs():
    """Collect all lm_unified_bugs into a validation-ready format."""
    bugs = []

    # Find all bug files recursively
    bug_files = list(BUG_GEN_DIR.rglob("bug__lm_unified_bugs*.diff"))

    print(f"Found {len(bug_files)} bug files")

    for bug_file in bug_files:
        # Get corresponding metadata file
        metadata_file = bug_file.parent / bug_file.name.replace("bug__", "metadata__").replace(".diff", ".json")

        if not metadata_file.exists():
            print(f"Metadata not found for {bug_file}")
            continue

        try:
            # Read the patch
            with open(bug_file) as f:
                patch = f.read()

            # Read metadata
            with open(metadata_file) as f:
                metadata = json.load(f)

            # Extract info from path
            # Structure: logs/bug_gen/vllm-project__vllm.3e1ad443/<encoded_path>/<func_hash>/bug__xxx.diff
            func_hash_dir = bug_file.parent.name
            encoded_path_dir = bug_file.parent.parent.name

            # Parse the encoded file path
            # Format: vllm-project__vllm.3e1ad443__path__to__file.py
            parts = encoded_path_dir.split("__")
            if len(parts) >= 3:
                # Skip first two parts (repo name)
                file_path = "/".join(parts[2:])
            else:
                file_path = encoded_path_dir

            # Extract function name from hash directory
            func_name = func_hash_dir.rsplit("_", 1)[0] if "_" in func_hash_dir else func_hash_dir

            # Create instance ID
            instance_id = f"vllm.3e1ad443.{func_name}.{func_hash_dir}"

            bug_entry = {
                "instance_id": instance_id,
                "repo": "vllm-project__vllm.3e1ad443",
                "patch": patch,
                "file_path": file_path,
                "function_name": func_name,
                "explanation": metadata.get("explanation", ""),
                "bug_type": metadata.get("explanation", "").split("\n")[0] if metadata.get("explanation") else "unknown",
            }

            bugs.append(bug_entry)

        except Exception as e:
            print(f"Error processing {bug_file}: {e}")
            import traceback
            traceback.print_exc()
            continue

    return bugs

def main():
    print("Collecting bugs for validation...")
    bugs = collect_bugs()

    print(f"\nCollected {len(bugs)} bugs")

    # Save to JSON
    with open(OUTPUT_FILE, "w") as f:
        json.dump(bugs, f, indent=2)

    print(f"Saved to {OUTPUT_FILE}")

    # Print summary
    print("\n=== Bug Collection Summary ===")
    print(f"Total bugs collected: {len(bugs)}")

    if bugs:
        # Count by module
        modules = {}
        for bug in bugs:
            fp = bug["file_path"]
            mod = fp.split("/")[0] if "/" in fp else fp.split(".")[0] if "." in fp else "unknown"
            modules[mod] = modules.get(mod, 0) + 1

        print("\nBugs by top-level module:")
        for mod, count in sorted(modules.items(), key=lambda x: -x[1]):
            print(f"  {mod}: {count}")

        # Show sample bug
        print("\n=== Sample Bug ===")
        sample = bugs[0]
        print(f"Instance ID: {sample['instance_id']}")
        print(f"File: {sample['file_path']}")
        print(f"Function: {sample['function_name']}")
        print(f"Bug Type: {sample['bug_type'][:100]}...")
        print(f"Patch length: {len(sample['patch'])} chars")

if __name__ == "__main__":
    main()
